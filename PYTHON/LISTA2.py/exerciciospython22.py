print("Este número irá corresponder aos dias da semana!")
x = int(input("\nEntão digite um número inteiro (de 1 ao 7): "))

if(x == 1):
    print("\nO DIA É DOMINGO!!")
elif(x == 2):
    print("O DIA É SEGUNDA-FEIRA!!")
elif(x == 3):
    print("O DIA É TERÇA-FEIRA!!")
elif(x == 4):
    print("O DIA É QUARTA-FEIRA!!")
elif(x == 5):
    print("O DIA É QUINTA-FEIRA!!")
elif(x == 6):
    print("O DIA É SEXTA-FERIA!!")
elif(x == 7):
    print("O DIA É SABÁDO!!")
else:
    print("NÚMERO DIGITADO INVÁLIDO!!!")
