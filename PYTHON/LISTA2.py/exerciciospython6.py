x = input("Olá, pode me falar o turno? M para matutino, V para vespertino e N para noturno: ")
if (x == "M" or x == "m"):
    print("Olá, BOM DIA!!")

elif (x == "V" or x == "v"):
    print("Olá, BOA TARDE!!")
elif (x == "N" or x == "n"):
    print("Olá, BOA NOITE!!")

else:
    print("VALOR INVÁLIDO!!!!!!!!")    